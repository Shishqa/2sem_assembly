;;=========================================================================
;; Custom putc implementation
;;=========================================================================

            section .text

            global  _put_c

;;=========================================================================
;; Writes char symbol to the console
;; Entry: dil <- char value
;; Destr: rax, rdi, dil, rdx
;;=========================================================================

_put_c:
            mov     rax, 1
            mov     byte [CharBuff], dil
            mov     rsi, CharBuff
            mov     rdi, 1
            mov     rdx, 1
            syscall

            ret

;;-------------------------------------------------------------------------

            section .data

CharBuff:   db 0

